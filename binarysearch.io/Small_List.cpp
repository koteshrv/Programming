/*
Given a list of integers nums, return whether its length is 1.

Example 1
Input

nums = [3]
Output

true
Example 2
Input

nums = [0, 9]
Output

false

*/

#include "solution.hpp"
#include <bits/stdc++.h>
using namespace std;


bool Solution::solve(std::vector<int>& nums) {
    // Write your code here
    if(nums.size()==1)
        return true;
    return false;
};
